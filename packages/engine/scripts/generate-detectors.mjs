// Generate validated detectors from rules/extra.json. Each rule is checked against
// its own fixtures (positives must match, negatives must not — an automatic
// false-positive gate); only passing rules are emitted. Fails the build if any
// rule's fixtures don't hold, so a bad pattern can never ship silently.
import { readFile, writeFile, mkdir } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const dir = dirname(fileURLToPath(import.meta.url))
const rulesPath = join(dir, '..', 'rules', 'extra.json')
const outPath = join(dir, '..', 'src', 'detectors', 'generated.ts')

// Expand {char:count} templates so fixtures don't need hand-counted strings.
const expand = (s) => s.replace(/\{([A-Za-z0-9]):(\d+)\}/g, (_, ch, n) => ch.repeat(Number(n)))

const rules = JSON.parse(await readFile(rulesPath, 'utf8'))
const errors = []
const passing = []

for (const r of rules) {
  const positives = (r.positives ?? []).map(expand)
  const negatives = (r.negatives ?? []).map(expand)
  if (positives.length < 2 || negatives.length < 2) {
    errors.push(`${r.id}: needs >=2 positives and >=2 negatives`)
    continue
  }
  let test
  try {
    test = new RegExp(r.pattern)
  } catch (e) {
    errors.push(`${r.id}: invalid regex — ${e.message}`)
    continue
  }
  let bad = false
  for (const p of positives) {
    if (!test.test(p)) {
      errors.push(`${r.id}: positive does not match: ${JSON.stringify(p).slice(0, 60)}`)
      bad = true
    }
  }
  for (const n of negatives) {
    if (test.test(n)) {
      errors.push(`${r.id}: negative wrongly matches (false positive): ${JSON.stringify(n).slice(0, 60)}`)
      bad = true
    }
  }
  if (!bad) passing.push({ ...r, positives, negatives })
}

if (errors.length) {
  console.error('generate-detectors: validation FAILED:')
  for (const e of errors) console.error('  - ' + e)
  process.exit(1)
}

const consts = passing
  .map((r, i) => `const RE_${i} = new RegExp(${JSON.stringify(r.pattern)}, ${JSON.stringify(r.flags ?? 'g')})`)
  .join('\n')

const items = passing
  .map(
    (r, i) => `  {
    id: ${JSON.stringify(r.id)},
    label: ${JSON.stringify(r.label)},
    severity: ${JSON.stringify(r.severity ?? 'critical')},
    defaultEnabled: ${(r.severity ?? 'critical') === 'critical'},
    scan: (text) => matchAll(RE_${i}, text),
    fixtures: { positives: ${JSON.stringify(r.positives)}, negatives: ${JSON.stringify(r.negatives)} },
  },`,
  )
  .join('\n')

const out = `// AUTO-GENERATED by scripts/generate-detectors.mjs from rules/extra.json.
// Do not edit by hand — change the rules file and re-run \`npm run gen\`.
import type { Detector } from '../types.js'
import { matchAll } from './_util.js'

${consts}

export const generated: Detector[] = [
${items}
]
`

await mkdir(dirname(outPath), { recursive: true })
await writeFile(outPath, out)
console.log(`generate-detectors: ${passing.length} detector(s) generated, ${errors.length} rejected`)
